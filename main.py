# Dobot初期化
m_lite = Dobot()

print("ホームポジションに移動します")
m_lite.set_homecmd()

# 動作
print(f"{len(coordinates)}個の座標を認識しました")

for i, coord in enumerate(coordinates, 1):
    print(f"\n座標{i}/{len(coordinates)}に移動中...")
    print(f"  x={coord['x']}, y={coord['y']}, z={coord['z']}, r={coord['r']}")
    
    result = m_lite.set_ptpcmd(
        ptp_mode=2,
        x=coord['x'],
        y=coord['y'],
        z=coord['z'],
        r=coord['r']
    )
    
    if result:
        print(f"  → 成功")
    else:
        print(f"  → 失敗 (射程外もしくは無理な姿勢です...)")

print("\n全座標の追従完了")
